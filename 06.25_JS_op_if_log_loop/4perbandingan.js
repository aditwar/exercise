// COMPARISON PEMBANDING

console.log(5 == "5"); // dilihat dari VALUE apakah sama
console.log(5 === "5"); // dilihat dari VALUE DAN TYPE apakah sama, hasilkan false karena satunya angka satunya STRING

// != NOT EQUAL VALUE, membandingkan value nya saja apakah sama
console.log(5 != 8); // hasil true

// !== NOT EQUAL VALUE n TYPE
console.log(5 !== 5); // hasil false
console.log(5 !== "5"); // hasil true
console.log(5 !== 8); // hasil true

// >< GREATER THAN or LESS THAN
console.log(5 > 8); // hasil false
console.log(5 < 8); // hasil true
console.log(5 >= 8); // hasil false
console.log(5 <= 8); // hasil true